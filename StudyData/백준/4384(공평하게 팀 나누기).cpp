int GCD(int a, int b)
{
    if (b == 0)
        return a;
    else
        return GCD(b, a % b);
}

int Ceil(double n)
{
    if (n - (int)(n) > 0)
        return (int)n + 1;
    else
        return n;
}

const int INF = 8999999999999999999;
const int Dic[4][2] = { {+1,+0},{-1,+0},{0,+1},{0,-1} };

////////////////////////////////////////////////////////////////////////

using namespace std;

int N;
int K[101];
//DP[선수수][첫번째팀합]
bool DP[51][50000];
int Sum = 0;
int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    cin >> N;
    for (int i = 0; i < N; i++)
    {
        cin >> K[i];
        Sum += K[i];
    }

    for (int j = 0; j < 51; j++)
        for (int k = 0; k < 50000; k++)
            DP[j][k] = false;

    DP[0][0] = true;
    for (int i = 0; i < N; i++)
        for (int j = N/2; j >= 1; j--)
            for (int k = 50000; k >= K[i]; k--)
                DP[j][k] = DP[j][k] || DP[j - 1][k - K[i]];

    int dis = INF;
    pair<int, int> Result = { 0,0 };

    for (int k = 0; k < 50000; k++)
    {
        if (DP[N / 2][k])
        {
            int a = k;
            int b = Sum - a;
            if (dis > abs(a - b))
            {
                dis = abs(a - b);
                Result = { a,b };
            }
        }
    }

    Debug(min(Result.first, Result.second), max(Result.first, Result.second));
}