using namespace std;

int N;
int P[50];
vector<int> C[50];
int DP[50];

int DFS(int n)
{
    if (DP[n] != -1)
        return DP[n];
    DP[n] = 0;
    vector<int> Arr;
    for (int i = 0; i < C[n].size(); i++)
        Arr.push_back(-DFS(C[n][i]));
    sort(Arr.begin(), Arr.end());
    for (int i = 0; i < Arr.size(); i++)
        Arr[i] = -Arr[i];
    for (int i = 0; i < Arr.size(); i++)
        DP[n] = max(DP[n], Arr[i] + 1 + i);
    return DP[n];
}


int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    cin >> N;

    for (int i = 0; i < N; i++)
    {
        cin >> P[i];
        if(P[i] != -1)
            C[P[i]].push_back(i);
    }

    Init(DP, -1);

    cout << DFS(0) << endl;
}
