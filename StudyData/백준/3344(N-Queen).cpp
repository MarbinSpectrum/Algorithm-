using namespace std;

int N;
bool flag = false;

int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    cin >> N;
    flag = N & 1;

    if ((!flag && N % 6 != 2) || (flag && (N - 1) % 6 != 2))
    {
        if (flag)
            N--;
        for (int i = 1; i <= N / 2; i++)
            cout << i * 2 << endl;
        for (int i = 1; i <= N / 2; i++)
            cout << i * 2 - 1 << endl;
        if (flag)
            cout << N + 1 << endl;
    }
    else
    {
        if (flag)
            N--;
        for (int i = 1; i <= N / 2; i++)
            cout << 1 + (i * 2 + N / 2 - 3) % N << endl;
        for (int i = N/2; i > 0; i--)
            cout << N - (i * 2 + N / 2 - 3) % N << endl;
        if (flag)
            cout << N + 1 << endl;
    }
   
}
