using namespace std;

int N;

int GN[3000];
int Visit[3000][3000];

int G(int len)
{
    if (GN[len] != -1)
        return GN[len];
    if (len == 0)
    {
        GN[len] = 0;
        return GN[len];
    }
    for (int i = 1; i <= len; i++)
    {
        int a = G(max(i - 3, (long long)0));
        int b = G(max(len - (i + 2), (long long)0));
        Visit[len][a ^ b] = true;
    }
    int c = 0;
    while (true)
    {
        if (!Visit[len][c])
            break;
        else
            c++;
    }
    GN[len] = c;
    return c;
}



int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    for (int i = 0; i < 3000; i++)
        GN[i] = -1;

    cin >> N;

    if (G(N) == 0)
        cout << 2 << endl;
    else
        cout << 1 << endl;

}
