using namespace std;

vector<pair<int, int>> Point;
int ResultValue = 999999999999;
pair<int, int> Result;

int Cal(pair<int, int> A, pair<int, int> B)
{
	return (A.first - B.first) * (A.first - B.first) + (A.second - B.second) * (A.second - B.second);
}

int32_t main()
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	int N;
	cin >> N;

	for (int i = 0; i < N; i++)
	{
		int a, b;
		cin >> a >> b;
		Point.push_back({ a,b });
	}

	if (N == 1)
	{
		Debug(Point[0].first, Point[0].second);

		return 0;
	}

	for (int i = 0; i < N; i++)
	{
		int Check = 0;
		for (int j = 0; j < N; j++)
		{
			if (i == j)
				continue;
			int Dis = Cal(Point[i], Point[j]);
			Check = max(Check, Dis);
		}

		if (ResultValue > Check)
		{
			Result = Point[i];
			ResultValue = Check;
		}
	}

	Debug(Result.first, Result.second);

}
