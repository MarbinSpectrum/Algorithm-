////////////////////////////////////////////////////////////////////////

using namespace std;

vector<int> Prime;
int N;
int Arr[3000001];
bool PrimeCheck[2000001];
int Check[3000001];
int32_t main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    for (int i = 2; i <= 1000000; i++) 
    {
        if (!PrimeCheck[i])
            Prime.push_back(i);
        for (int j = 0; j < Prime.size() && i * Prime[j] < 1000000; j++) 
        {
            PrimeCheck[i * Prime[j]] = true;
            if (i % Prime[j] == false)
                break;
        }
    }

    cin >> N;

    for (int i = 0; i < N; i++)
        cin >> Arr[i];

    int result = 0;
    for (int i = 0; i < Prime.size(); i++)
    {
        int primeNum = Prime[i];

        for (int j = 0; j < N; j++)
        {
            Check[Arr[j] % primeNum]++;
            result = max(result, Check[Arr[j] % primeNum]);
        }
        for (int j = 0; j < N; j++)
            Check[Arr[j] % primeNum]--;
    }
    cout << result << endl;
}