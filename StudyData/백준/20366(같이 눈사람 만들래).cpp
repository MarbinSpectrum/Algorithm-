const int INF = 8999999999999999999;
const int Dic[4][2] = { {+1,+0},{-1,+0},{0,+1},{0,-1} };

////////////////////////////////////////////////////////////////////////

using namespace std;

int N;
vector<int> H;
vector<tuple<int, int, int>> Arr;
int Result = INF;

int32_t main()
{
	ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);

	cin >> N;

	for (int i = 0; i < N; i++)
	{
		int a;
		cin >> a;
		H.push_back(a);
	}

	sort(H.begin(), H.end());

	for (int i = 0; i < H.size(); i++)
		for (int j = i + 1; j < H.size(); j++)
		{
			int a = i;
			int b = N - 1;
			while (true)
			{
				while (a == i || a == j)
					a++;
				while (b == i || b == j)
					b--;
					
				if (a >= b)
					break;
				//Debug(a, b);
				int snowMan1 = H[i] + H[j];
				int snowMan2 = H[a] + H[b];
				if (snowMan1 < snowMan2)
				{
					Result = min(Result, abs(snowMan1 - snowMan2));
					b--;
				}
				else
				{
					Result = min(Result, abs(snowMan1 - snowMan2));
					a++;
				}
			}
		}

	cout << Result << endl;
}
