int Mul(int x, int y, int mod)
{
	return x * y % mod;
}

int GCD(int a, int b)
{
	if (a < b)
		swap(a, b);

	while (b != 0)
	{
		int r = a % b;
		a = b;
		b = r;
	}
	return a;
}

int LCM(int a, int b)
{
	return a * b / GCD(a, b);
}
int Ceil(int a, int b)
{
	return (a % b == 0) ? (a / b) : (a / b + 1);
}
int Ceil(double n)
{
	if (n - (int)(n) > 0)
		return (int)n + 1;
	else
		return n;
}

int Pow(int a, int b)
{
	int res = 1;
	while (b > 0)
	{
		if (b & 1)
			res = (res * a);
		b = b >> 1;
		a = (a * a);
	}
	return res;
}
int Pow(int a, int b, int p)
{
	int res = 1;
	a = a % p;
	while (b > 0)
	{
		if (b & 1)
			res = Mul(res, a, p);
		b = b >> 1;
		a = Mul(a, a, p);
	}
	return res;
}

vector<string> split(string input, vector<char> check)
{
	vector<string> answer;
	input += check[0];
	string temp = "";
	for (int i = 0; i < input.size(); i++)
	{
		bool flag = false;
		for (int j = 0; j < check.size(); j++)
			if (input[i] == check[j])
			{
				flag = true;
				break;
			}
		if (flag && temp.size() > 0)
		{
			answer.push_back(temp);
			temp = "";
		}
		else
		{
			temp += input[i];
		}
	}
	return answer;
}

const int INF = 0x3f3f3f3f;
const int Dic[4][2] = { {+1,+0},{-1,+0},{0,+1},{0,-1} };

////////////////////////////////////////////////////////////////////////

int N, M;
int MAP[51][51];
bool Visit[51][51][51][51];
pair<int, int> ans = { 0,0 };
void F(int r, int c)
{
	queue<pair<pair<int, int>,int>> Q;
	if (MAP[r][c] != 0)
	{
		Q.push({ { r,c },0 });
		Visit[r][c][r][c] = true;
	}
	while (!Q.empty())
	{
		pair<pair<int, int>, int> temp = Q.front();
		Q.pop();
		if (ans.second < temp.second)
		{
			ans.second = temp.second;
			ans.first = MAP[r][c] + MAP[temp.first.first][temp.first.second];
		}
		else if (ans.second == temp.second)
		{
			ans.first = max(ans.first,MAP[r][c] + MAP[temp.first.first][temp.first.second]);
		}

		for (int i = 0; i < 4; i++)
		{
			int ar = temp.first.first + Dic[i][0];
			int ac = temp.first.second + Dic[i][1];
			if (0 <= ar && ar < N && 0 <= ac && ac < M)
			{
				if (MAP[ar][ac] != 0 && Visit[r][c][ar][ac] == false)
				{
					Q.push({ { ar,ac },temp.second + 1 });
					Visit[r][c][ar][ac] = true;
				}
			}
		}
	}
}

int32_t main()
{
	ios_base::sync_with_stdio(false);
	std::cin.tie(NULL);
	std::cout.tie(NULL);

	std::cin >> N >> M;
	for (int i = 0; i < N; i++)
	{
		for (int j = 0; j < M; j++)
		{
			std::cin >> MAP[i][j];
		}
	}

	for (int i = 0; i < N; i++)
	{
		for (int j = 0; j < M; j++)
		{
			F(i, j);
		}
	}

	std::cout << ans.first << endl;
}