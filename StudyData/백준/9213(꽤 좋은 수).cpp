const int MAX = 1000000;
int A[MAX + 1];

int Get(int n)
{
	int sum = 1;
	int stack = 1;
	int now = 0;
	while (n != 1)
	{
		if (now != A[n])
		{
			now = A[n];
			stack = sum;
		}
		stack *= now;
		sum += stack;

		n /= A[n];
	}

	return sum;

}

